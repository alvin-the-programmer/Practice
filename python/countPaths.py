def countPaths(a):
	ways = {(0, 0): 1}
	return countPath(len(a) - 1, len(a[0]) - 1, a, ways)

def countPath(i, j, a, ways):
	if i < 0 or j < 0:
		return 0
	if a[i][j] == 0:
		return 0
	if (i, j) in ways:
		return ways[(i, j)]
	count = countPath(i - 1, j, a, ways) + countPath(i, j - 1, a, ways)
	ways[(i, j)] = count
	return count

